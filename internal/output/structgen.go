// NewStructGenerator creates a new structure generator
func NewStructGenerator(meta *metadata.GlobalMetadata, types []*binary.Il2CppType, version float64) *StructGenerator {
	return &StructGenerator{
		meta:                    meta,
		types:                   types,
		version:                 version,
		structNameDic:           make(map[int]string),
		genericClassStructNames: make(map[uint64]string),
		structNameHashSet:       make(map[string]bool),
		typeDefImageNames:       make(map[int]string),
		genericInstNames:        make(map[int]string),
		vtableMethodNames:       make(map[int]map[int]string),
		arrayTypeNames:          make(map[int]string),
		arrayTypeElements:       make(map[int]string),
	}
}

// NewStructGeneratorWithAnalyzer creates a structure generator with a static analyzer
// for reading generic class data from the binary
func NewStructGeneratorWithAnalyzer(meta *metadata.GlobalMetadata, types []*binary.Il2CppType, version float64, analyzer *binary.IL2CPPStaticAnalyzer) *StructGenerator {
	g := NewStructGenerator(meta, types, version)
	g.staticAnalyzer = analyzer
	return g
}

// SetRGCTXData sets the RGCTX data parsed from the binary.
// This enables proper _RGCTXs struct generation with actual class/method references.
func (g *StructGenerator) SetRGCTXData(rgctx *RGCTXData) {
	g.rgctxData = rgctx
}

// GenerateHeader generates the il2cpp.h content
func (g *StructGenerator) GenerateHeader() string {
	var sb strings.Builder

	// Write header
	sb.WriteString("// Auto-generated IL2CPP structures\n")
	sb.WriteString("// Generated by disunity\n\n")
	sb.WriteString("#pragma once\n\n")
	sb.WriteString("#include <stdint.h>\n")
	sb.WriteString("#include <stdbool.h>\n")
	sb.WriteString("#include <stddef.h>\n\n")

	// Write base IL2CPP types
	g.writeBaseTypes(&sb)

	// Build unique struct names for all type definitions
	g.buildStructNames()

	// Build vtable method name mappings
	g.buildVTableMethodNames()

	// Build generic instantiation names
	g.buildGenericInstNames()

	// Build array type names (SZARRAY types)
	g.buildArrayTypeNames()

	// Generate structs for all types
	g.generateAllStructs(&sb)

	// Generate array type structs
	g.generateArrayStructs(&sb)

	// Generate generic instantiation structs
	g.generateGenericInstStructs(&sb)

	return sb.String()
}

// ClassCount returns the number of generated struct names.
// Must be called after GenerateHeader().
func (g *StructGenerator) ClassCount() int {
	return len(g.structNameDic)
}

