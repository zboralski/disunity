// StructGenerator generates il2cpp.h from metadata and binary type information
type StructGenerator struct {
	meta    *metadata.GlobalMetadata
	types   []*binary.Il2CppType
	version float64

	// Name mappings
	structNameDic            map[int]string            // typeDefIndex -> struct name
	genericClassStructNames  map[uint64]string         // generic_class ptr -> struct name
	structNameHashSet        map[string]bool           // track generated struct names
	typeDefImageNames        map[int]string            // typeDefIndex -> image name
	genericInstNames         map[int]string            // type index -> generic instantiation name

	// VTable method names (slot index -> method name)
	vtableMethodNames map[int]map[int]string // typeDefIndex -> slot -> methodName

	// Array types tracking (type index -> array struct name)
	arrayTypeNames map[int]string        // SZARRAY type index -> array struct name
	arrayTypeElements map[int]string     // SZARRAY type index -> element type name

	// Generated output
	structInfoList []StructInfo

	// Static analyzer for reading generic class data from binary
	staticAnalyzer *binary.IL2CPPStaticAnalyzer

	// RGCTX data parsed from binary (for proper _RGCTXs generation)
	rgctxData *RGCTXData
}

// StructInfo holds information about a generated struct
type StructInfo struct {
	TypeName   string
	IsClass    bool
	Fields     []FieldInfo
	Parent     string
}

// FieldInfo holds information about a struct field
type FieldInfo struct {
	TypeName   string
	FieldName  string
	Offset     int32
}

