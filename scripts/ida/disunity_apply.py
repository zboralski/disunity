def build_class_struct(sname, size, fields, pointer_size):
    """Build an IL2CPP class struct as C declaration."""
    field_sz = pointer_size
    field_type = "int" if pointer_size == 4 else "void*"

    sorted_fields = sorted(fields, key=lambda f: f.get("byte_offset", 0))

    lines = ["struct %s {" % sname]
    prev_end = 0
    pad_idx = 0
    for f in sorted_fields:
        off = f.get("byte_offset", 0)
        fname = sanitize(f.get("name", "field_%x" % off))
        tname = f.get("type_name", "")
        if off < prev_end:
            continue
        if off > prev_end:
            lines.append("  char _pad%d[%d];" % (pad_idx, off - prev_end))
            pad_idx += 1
        # Use type info if available
        ctype = il2cpp_type_to_c(tname, field_type)
        if off + type_size(ctype, pointer_size) <= size:
            lines.append("  %s %s;" % (ctype, fname))
            prev_end = off + type_size(ctype, pointer_size)
        else:
            prev_end = off

    if prev_end < size:
        lines.append("  char _pad_tail[%d];" % (size - prev_end))

    lines.append("};")
    return "\n".join(lines)


def il2cpp_type_to_c(type_name, default):
    """Map IL2CPP type name to C type."""
    mapping = {
        "System.Boolean": "char",
        "System.Byte": "unsigned char",
        "System.SByte": "char",
        "System.Int16": "short",
        "System.UInt16": "unsigned short",
        "System.Int32": "int",
        "System.UInt32": "unsigned int",
        "System.Int64": "long long",
        "System.UInt64": "unsigned long long",
        "System.Single": "float",
        "System.Double": "double",
        "System.Char": "unsigned short",
        "System.IntPtr": "void*",
        "System.UIntPtr": "void*",
    }
    return mapping.get(type_name, default)


def type_size(ctype, pointer_size):
    """Return size of a C type."""
    sizes = {
        "char": 1,
        "unsigned char": 1,
        "short": 2,
        "unsigned short": 2,
        "int": 4,
        "unsigned int": 4,
        "long long": 8,
        "unsigned long long": 8,
        "float": 4,
        "double": 8,
        "void*": pointer_size,
    }
    return sizes.get(ctype, pointer_size)


def build_function_prototype(name, owner, param_count, return_type, is_static, struct_names):
    """Build C function prototype for IL2CPP method."""
    cname = sanitize(name)
    ret = il2cpp_type_to_c(return_type, "void*") if return_type else "void*"
    params = []

    # IL2CPP instance methods get a this pointer + MethodInfo*
    if owner and not is_static:
        if owner in struct_names:
            params.append("struct %s* this" % struct_names[owner])
        else:
            params.append("void* this")

    for i in range(param_count):
        params.append("void* p%d" % i)

    # IL2CPP always passes MethodInfo* as last hidden arg
    params.append("void* method")

    return "%s __fastcall %s(%s);" % (ret, cname, ", ".join(params))


if __name__ == "__main__":
    main()
